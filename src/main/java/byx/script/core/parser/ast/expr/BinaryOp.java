package byx.script.core.parser.ast.expr;

/**
 * 二元运算符
 */
public enum BinaryOp {
    Add, // +
    Sub, // -
    Mul, // *
    Div, // /
    Rem, // %
    LessThan, // <
    LessEqualThan, // <=
    GreaterThan, // >
    GreaterEqualThan, // >=
    Equal, // ==
    NotEqual, // !=
    And, // &&
    Or // ||
}
